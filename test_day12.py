from day12 import Plot, distinct_edges, Regions, Region


def test_segment():
    def _check(plots, *expected_regions):
        region = Region(plots)
        expected_regions = set(map(frozenset, expected_regions))
        assert set(map(frozenset, region.segment())) == expected_regions

    _check(
        {
            Plot(1, 1),
            Plot(1, 2),
        },
        {
            Plot(1, 1),
            Plot(1, 2),
        },
    )

    _check(
        {
            Plot(1, 1),
            Plot(3, 3),
        },
        {Plot(1, 1)},
        {Plot(3, 3)},
    )

    _check(
        {
            Plot(1, 1),
            Plot(1, 2),
            Plot(1, 3),
            Plot(2, 3),
            Plot(3, 1),
            Plot(4, 1),
        },
        {
            Plot(1, 1),
            Plot(1, 2),
            Plot(1, 3),
            Plot(2, 3),
        },
        {
            Plot(3, 1),
            Plot(4, 1),
        },
    )


