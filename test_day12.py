def test_distinct_edges():
    boundaries = [
        (True, False),
        (False, False),
        (True, False),
        (True, False),
        (False, True),
        (True, True),
        (False, False),
        (False, True),
    ]
    assert distinct_edges(boundaries) == 4


def test_count_edges():
    regions = Regions.parse(test_input_simple)
    assert regions.containing(Plot(0, 0)).count_edges() == 4  # A
    assert regions.containing(Plot(0, 1)).count_edges() == 4  # B
    assert regions.containing(Plot(2, 1)).count_edges() == 8  # C
    assert regions.containing(Plot(3, 1)).count_edges() == 4  # D
